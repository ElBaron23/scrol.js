/**
 * ----------------------------------------------------------------------------
 * الفئة (Class) الرئيسية: Scrol
 * ----------------------------------------------------------------------------
 * الكلاس (Class) هو عبارة عن قالب أو مصنع نضع فيه كل الأكواد التي تخص 
 * عمل المكتبة. بهذه الطريقة نجمع الأكواد بشكل منظم يسهل استخدامه لاحقاً.
 */
class Scrol {
    
    /**
     * ------------------------------------------------------------------------
     * الدالة البانية (Constructor)
     * ------------------------------------------------------------------------
     * دالة `constructor` هي دالة خاصة ومهمة جداً، فهي أول دالة يتم تشغيلها
     * تلقائياً وبشكل فوري بمجرد أن يقوم المستخدم بتشغيل المكتبة عبر أمر:
     * new Scrol()
     * 
     * @param {Object} options - متغير يستقبل خيارات مخصصة من المستخدم (مثل المدة، التأثير). 
     *                           إذا لم يضع المستخدم خيارات، نستخدم كائناً فارغاً {} كافتراضي.
     */
    constructor(options = {}) {
        
        /**
         * حفظ الخيارات التي مررها المستخدم في الخاصية (this.options)
         * كلمة `this` تعني "هذا الكائن الحالي"، وهي طريقة لحفظ المتغيرات
         * بداخل هذا الكلاس ليتم استخدامها في دوال أخرى.
         */
        this.options = options;
        
        /**
         * تحديد الوعاء الرئيسي (Container)
         * --------------------------------------------------------------------
         * الوعاء هو العنصر الأب الذي يضم كل الأقسام (Sections) بداخله.
         * هنا نستخدم دالة `document.querySelector` للبحث عن العنصر في صفحة HTML.
         * نأخذ القيمة من خيارات المستخدم `this.options.container`.
         * إذا لم يحدد المستخدم وعاء، نستخدم العلامة المزدوجة `||` كطريقة للقول:
         * "أو استخدم وسم 'body' كخيار افتراضي".
         */
        this.container = document.querySelector(this.options.container || 'body');
        
        /**
         * تحديد جميع الأقسام (Sections)
         * --------------------------------------------------------------------
         * الأقسام هي الصفحات الفرعية أو الشاشات التي سنتنقل بينها.
         * نستخدم `querySelectorAll` للبحث داخل الوعاء الرئيسي الذي حددناه بالأعلى.
         * إما أن نستخدم اسم الوسم المخصص الذي أدخله المستخدم `this.options.selector`،
         * أو نستخدم وسم 'section' كخيار افتراضي لكل الأقسام.
         */
        this.sections = this.container.querySelectorAll(this.options.selector || 'section');
        
        /**
         * رقم القسم الحالي (Current Section Index)
         * --------------------------------------------------------------------
         * الفهرس (Index) في البرمجة يبدأ عادة من الرقم (0) وليس (1).
         * لذلك، الرقم 0 يعني "القسم الأول"، والرقم 1 يعني "القسم الثاني" وهكذا.
         * هذا المتغير سيساعدنا في معرفة في أي قسم نحن موجودون الآن.
         */
        this.currentSectionIndex = 0;
        
        /**
         * حالة التمرير (Is Scrolling)
         * --------------------------------------------------------------------
         * هذا المتغير هو بمثابة "حارس" أو "قفل".
         * نعطيه القيمة `false` (يعني غير مقفل) في البداية.
         * فائدته: يمنع المستخدم من التمرير مرتين بسرعة كبيرة. فإذا كانت
         * هناك حركة انتقال تحدث الآن، لن نسمح ببدء حركة جديدة حتى تنتهي الأولى.
         */
        this.isScrolling = false;
        
        /**
         * مدة حركة الانتقال (Transition Duration)
         * --------------------------------------------------------------------
         * كم من الوقت (بالميلي ثانية) يجب أن تستغرق الحركة بين قسم وآخر؟
         * نأخذ القيمة من إعدادات المستخدم، أو نستخدم `800` ميلي ثانية كافتراضي.
         * (ملاحظة: 1000 ميلي ثانية = 1 ثانية كاملة، لذا 800 يعني أقل من ثانية بقليل).
         */
        this.transitionDuration = this.options.duration || 800;
        
        /**
         * نوع التأثير الحركي (Effect)
         * --------------------------------------------------------------------
         * ما هو شكل الحركة؟ هل هو انزلاق (slide) أو ربما ظهور تدريجي (fade)؟
         * نأخذ القيمة من المستخدم، وإذا لم يحدد شيئاً نستخدم 'slide' كافتراضي.
         */
        this.effect = this.options.effect || 'slide';
        
        /**
         * بدء التشغيل الفعلي (Initialization)
         * --------------------------------------------------------------------
         * بعد أن قمنا بضبط وإعداد كل المتغيرات السابقة، أصبحنا جاهزين للعمل!
         * نقوم الآن باستدعاء الدالة `init()` لتقوم بتهيئة الصفحة وبدء تطبيق التأثيرات.
         */
        this.init();
    }

    /**
     * ------------------------------------------------------------------------
     * دالة التهيئة والتشغيل (Init)
     * ------------------------------------------------------------------------
     * وظيفة هذه الدالة هي تجهيز عناصر HTML لكي تتوافق مع تصميم CSS.
     * هي تقوم بإضافة الأصناف (Classes) والسمات (Attributes) المطلوبة.
     */
    init() {
        
        /**
         * إضافة صنف للوعاء الرئيسي
         * --------------------------------------------------------------------
         * نضيف الصنف (class) الذي اسمه 'scrol-container' للوعاء الأب.
         * بمجرد إضافته، سيتعرف ملف CSS الخاص بنا عليه ويطبق عليه التنسيقات
         * الأساسية مثل منعه من إظهار أشرطة التمرير العادية (overflow: hidden).
         */
        this.container.classList.add('scrol-container');
        
        /**
         * تحديد نوع التأثير لملف CSS
         * --------------------------------------------------------------------
         * نضيف سمة برمجية تسمى 'data-effect' للوعاء الرئيسي.
         * نعطيها قيمة التأثير المخزنة في `this.effect` (مثل 'slide').
         * هذا يتيح لملف CSS معرفة نوع الحركة المطلوبة وتطبيقها.
         */
        this.container.setAttribute('data-effect', this.effect);

        /**
         * المرور على جميع الأقسام (Looping through sections)
         * --------------------------------------------------------------------
         * لدينا مجموعة من الأقسام، نحتاج إلى تجهيزها واحداً تلو الآخر.
         * نستخدم دالة `forEach` وهي عبارة عن حلقة تكرارية (Loop) تمر على
         * كل قسم. بداخلها نستقبل شيئين:
         * 1. `section`: القسم الحالي في الدورة (العنصر نفسه).
         * 2. `index`: رقم الفهرس لهذا القسم (0، 1، 2، 3...).
         */
        this.sections.forEach((section, index) => {
            
            /**
             * تجهيز كل قسم
             * نضيف الصنف 'scrol-section' لكل قسم لكي يأخذ التنسيقات 
             * الخاصة بالعرض والطول بحيث يغطي الشاشة بالكامل.
             */
            section.classList.add('scrol-section');
            
            /**
             * التحقق من القسم الأول
             * ----------------------------------------------------------------
             * إذا كان رقم الفهرس يساوي 0 (يعني أننا نقف على القسم الأول).
             * فهذا يعني أنه هو القسم الذي يجب أن يظهر للمستخدم أولاً عند فتح الصفحة.
             */
            if (index === 0) {
                
                /**
                 * إظهار القسم الأول
                 * نضيف إليه صنف 'scrol-active' وهو الصنف المسؤول
                 * عن إظهار القسم على الشاشة (باقي الأقسام ستكون مخفية).
                 */
                section.classList.add('scrol-active');
            }
        });

        /**
         * تشغيل مستمعات الأحداث (Event Listeners)
         * --------------------------------------------------------------------
         * بعد أن جهزنا الأقسام، الصفحة الآن ثابتة لا تتحرك!
         * لذا نقوم باستدعاء هذه الدالة لكي نبدأ بمراقبة تحركات المستخدم 
         * (استخدام عجلة الماوس، لمس الشاشة، لوحة المفاتيح) للتفاعل معها.
         */
        this.addEventListeners();
    }

    /**
     * ------------------------------------------------------------------------
     * دالة مراقبة وتتبع تفاعلات المستخدم (Add Event Listeners)
     * ------------------------------------------------------------------------
     * المستمعات (Listeners) هي مثل "أجهزة إنذار" نضعها في المتصفح،
     * مهمتها الانتظار بصمت حتى يقوم المستخدم بفعل معين (مثل تحريك الماوس)،
     * وبمجرد حدوث هذا الفعل، تقوم بتشغيل كود معين رداً على ذلك.
     */
    addEventListeners() {
        
        /**
         * 1. مراقبة عجلة الفأرة (Mouse Wheel)
         * --------------------------------------------------------------------
         * نراقب حدث يسمى 'wheel' وهو الخاص بحركة البكرة أو العجلة في الماوس.
         * عندما يحرك المستخدم العجلة، يتم تمرير تفاصيل الحدث (event) إلى
         * الدالة 'handleScroll' التي سنشرحها لاحقاً.
         * ملاحظة: الخيار `{ passive: false }` ضروري لكي نتمكن من إلغاء 
         * التمرير الافتراضي للمتصفح.
         */
        window.addEventListener('wheel', (event) => this.handleScroll(event), { passive: false });
        
        /**
         * 2. مراقبة لوحة المفاتيح (Keyboard)
         * --------------------------------------------------------------------
         * نراقب حدث يسمى 'keydown' ويحدث عندما يضغط المستخدم على أي زر
         * في لوحة المفاتيح. عند حدوثه، نرسل التفاصيل إلى دالة 'handleKeydown'.
         */
        window.addEventListener('keydown', (event) => this.handleKeydown(event));

        /**
         * متغير لتسجيل نقطة بداية اللمس على الشاشة
         * --------------------------------------------------------------------
         * شاشات الهواتف لا تمتلك "عجلة فأرة"، لذلك نعتمد على اللمس.
         * نحتاج أن نعرف: من أين بدأ المستخدم اللمس؟ نعطي المتغير قيمة 0 مبدئياً.
         */
        this.touchStartY = 0;
        
        /**
         * 3. مراقبة بداية اللمس (Touch Start)
         * --------------------------------------------------------------------
         * حدث 'touchstart' ينطلق في اللحظة التي يضع فيها المستخدم إصبعه 
         * على شاشة الهاتف.
         */
        window.addEventListener('touchstart', (event) => {
            /**
             * تسجيل نقطة البداية (Start Point)
             * نأخذ الإحداثي الصادي (Y) وهو المحور العمودي (من أعلى لأسفل).
             * نأخذه من الإصبع الأول `touches[0]`، ونخزنه في المتغير للرجوع إليه لاحقاً.
             */
            this.touchStartY = event.touches[0].clientY;
        }, { passive: false });

        /**
         * 4. مراقبة نهاية اللمس (Touch End)
         * --------------------------------------------------------------------
         * حدث 'touchend' ينطلق في اللحظة التي يرفع فيها المستخدم إصبعه من الشاشة.
         */
        window.addEventListener('touchend', (event) => {
            /**
             * تسجيل نقطة النهاية (End Point)
             * نأخذ الإحداثي الصادي (Y) للنقطة التي انتهى عندها اللمس ورفع الإصبع.
             */
            const touchEndY = event.changedTouches[0].clientY;
            
            /**
             * بعد أن حصلنا على نقطة البداية ونقطة النهاية، نرسلهما إلى
             * الدالة 'handleTouch' لتقوم بحساب المسافة وتحديد الاتجاه.
             */
            this.handleTouch(this.touchStartY, touchEndY);
        });
    }

    /**
     * ------------------------------------------------------------------------
     * دالة معالجة حركة عجلة الماوس (Handle Scroll)
     * ------------------------------------------------------------------------
     * تستقبل تفاصيل الحدث `event` الذي نتج عن تحريك عجلة الماوس.
     */
    handleScroll(event) {
        
        /**
         * إيقاف السلوك الافتراضي
         * --------------------------------------------------------------------
         * السلوك الطبيعي للمتصفح عند تحريك العجلة هو تحريك الصفحة قليلاً للأسفل
         * بضعة بكسلات. نحن لا نريد ذلك، بل نريد الانتقال لقسم كامل، لذلك
         * نوقف هذا السلوك الطبيعي.
         */
        event.preventDefault();
        
        /**
         * التحقق من "الحارس" أو "القفل"
         * --------------------------------------------------------------------
         * نسأل: هل هناك حركة تمرير تحدث الآن (isScrolling === true)؟
         * إذا كانت الإجابة نعم، نستخدم كلمة `return` للخروج من الدالة فوراً
         * وتجاهل حركة الماوس الجديدة.
         */
        if (this.isScrolling) return;

        /**
         * تحديد الاتجاه (Direction)
         * --------------------------------------------------------------------
         * الخاصية `deltaY` تخبرنا باتجاه وقوة الدوران.
         * 1. إذا كانت `deltaY` أكبر من صفر (موجبة): المستخدم يسحب العجلة للأسفل.
         * 2. إذا كانت `deltaY` أصغر من صفر (سالبة): المستخدم يسحب العجلة للأعلى.
         */
        if (event.deltaY > 0) {
            
            /**
             * الدوران للأسفل: نستدعي دالة "التحرك للأسفل" (للقسم التالي).
             */
            this.moveDown();
            
        } else if (event.deltaY < 0) {
            
            /**
             * الدوران للأعلى: نستدعي دالة "التحرك للأعلى" (للقسم السابق).
             */
            this.moveUp();
        }
    }

    /**
     * ------------------------------------------------------------------------
     * دالة معالجة أزرار لوحة المفاتيح (Handle Keydown)
     * ------------------------------------------------------------------------
     * تستقبل تفاصيل الحدث `event` لمعرفة أي زر تم الضغط عليه.
     */
    handleKeydown(event) {
        
        /**
         * كالعادة، نتحقق من قفل الحركة لمنع التكرار السريع.
         */
        if (this.isScrolling) return;

        /**
         * التحقق من الزر المضغوط
         * --------------------------------------------------------------------
         * الخاصية `event.key` تعطينا اسم الزر.
         * إذا كان الزر هو السهم السفلي ('ArrowDown')...
         */
        if (event.key === 'ArrowDown') {
            
            /**
             * نستدعي دالة التحرك للأسفل.
             */
            this.moveDown();
            
        /**
         * وإذا كان الزر هو السهم العلوي ('ArrowUp')...
         */
        } else if (event.key === 'ArrowUp') {
            
            /**
             * نستدعي دالة التحرك للأعلى.
             */
            this.moveUp();
        }
    }

    /**
     * ------------------------------------------------------------------------
     * دالة معالجة اللمس والسحب على الشاشة (Handle Touch)
     * ------------------------------------------------------------------------
     * تستقبل نقطة البداية ونقطة النهاية للمس لتقرر ماذا يجب أن تفعل.
     */
    handleTouch(startY, endY) {
        
        /**
         * نتحقق من قفل الحركة كالمعتاد لمنع التداخل.
         */
        if (this.isScrolling) return;

        /**
         * تحديد حد السحب (Threshold)
         * --------------------------------------------------------------------
         * لكي لا ننتقل لصفحة أخرى بمجرد لمس الشاشة لمسة خفيفة بالخطأ،
         * نضع شرطاً بأن مسافة السحب يجب أن تكون على الأقل 50 بكسل
         * لكي نعتبرها حركة "مقصودة".
         */
        const threshold = 50;
        
        /**
         * التحقق من اتجاه السحب
         * --------------------------------------------------------------------
         * الشاشة تبدأ إحداثياتها (Y=0) من الأعلى وتزيد كلما نزلنا للأسفل.
         * 
         * الحالة الأولى:
         * إذا كانت نقطة البداية (startY) أكبر من نقطة النهاية (endY) 
         * بمسافة تفوق الـ 50 بكسل.
         * هذا يعني أن المستخدم بدأ من الأسفل وسحب إصبعه للأعلى (يمسح الشاشة للأعلى).
         * عندما يمسح الشاشة للأعلى، فهو يريد رؤية المحتوى الذي بالأسفل!
         */
        if (startY - endY > threshold) {
            
            /**
             * لذا ننتقل إلى القسم الذي بالأسفل.
             */
            this.moveDown();
            
        /**
         * الحالة الثانية:
         * إذا كانت نقطة النهاية أكبر من البداية بمسافة تفوق الـ 50 بكسل.
         * هذا يعني أن المستخدم بدأ من الأعلى وسحب إصبعه للأسفل (يمسح للأسفل).
         * فهو يريد رؤية المحتوى الموجود في الأعلى.
         */
        } else if (endY - startY > threshold) {
            
            /**
             * لذا ننتقل إلى القسم الذي بالأعلى.
             */
            this.moveUp();
        }
    }

    /**
     * ------------------------------------------------------------------------
     * دالة التحرك للأسفل (Move Down)
     * ------------------------------------------------------------------------
     * مهمة هذه الدالة هي التأكد من إمكانية النزول، ثم طلب تنفيذ النزول.
     */
    moveDown() {
        
        /**
         * التحقق من النهاية (هل وصلنا لآخر قسم؟)
         * --------------------------------------------------------------------
         * `this.sections.length` يعطينا العدد الإجمالي للأقسام (مثلاً 4).
         * وبما أننا بدأنا العد من الصفر، فإن القسم الأخير رقمه هو (العدد الإجمالي - 1)
         * أي (4 - 1 = 3).
         * الشرط يقول: طالما أن رقم القسم الحالي أصغر من رقم القسم الأخير...
         * فهذا يعني أن هناك أقسام أخرى متبقية بالأسفل ويمكننا النزول.
         */
        if (this.currentSectionIndex < this.sections.length - 1) {
            
            /**
             * نستدعي دالة تحديث الشاشة، ونعطيها:
             * 1. رقم الفهرس الجديد (وهو الرقم الحالي + 1).
             * 2. اتجاه الحركة، وهو للأسفل ('down').
             */
            this.updateSections(this.currentSectionIndex + 1, 'down');
        }
    }

    /**
     * ------------------------------------------------------------------------
     * دالة التحرك للأعلى (Move Up)
     * ------------------------------------------------------------------------
     * مهمة هذه الدالة هي التأكد من إمكانية الصعود، ثم طلب تنفيذ الصعود.
     */
    moveUp() {
        
        /**
         * التحقق من البداية (هل نحن في القسم الأول؟)
         * --------------------------------------------------------------------
         * الشرط يقول: إذا كان الفهرس الحالي أكبر من صفر.
         * هذا يعني أننا لست في القسم الأول، بل في الثاني أو الثالث... 
         * بالتالي هناك أقسام سابقة فوقنا يمكننا الصعود إليها.
         */
        if (this.currentSectionIndex > 0) {
            
            /**
             * نستدعي دالة تحديث الشاشة، ونعطيها:
             * 1. رقم الفهرس الجديد (وهو الرقم الحالي - 1).
             * 2. اتجاه الحركة، وهو للأعلى ('up').
             */
            this.updateSections(this.currentSectionIndex - 1, 'up');
        }
    }

    /**
     * ------------------------------------------------------------------------
     * الدالة الجوهرية لتحديث الأقسام على الشاشة (Update Sections)
     * ------------------------------------------------------------------------
     * هذه الدالة هي العقل المدبر الذي ينفذ التغييرات المرئية بتبديل الأصناف (Classes).
     * تستقبل المتغيرات: الفهرس الجديد للقسم الذي سنذهب إليه، واتجاه الحركة.
     * 
     * @param {Number} newIndex - رقم القسم الجديد الذي يجب أن يظهر.
     * @param {String} direction - اتجاه الحركة 'up' أو 'down'.
     */
    updateSections(newIndex, direction) {
        
        /**
         * 1. إغلاق القفل لمنع حركات متداخلة
         * --------------------------------------------------------------------
         * نجعل قيمة متغير التمرير `true`، بمعنى "المكتبة مشغولة بحركة الآن".
         * أي محاولة تمرير جديدة سيتم تجاهلها بفضل هذا القفل.
         */
        this.isScrolling = true;

        /**
         * 2. جلب العناصر المطلوبة من مصفوفة الأقسام
         * --------------------------------------------------------------------
         * نحتاج أن نتعامل مع القسم الذي سنغادره (القسم الحالي)، 
         * والقسم الذي سنصل إليه (القسم القادم).
         */
        
        // جلب القسم القديم الذي كنا نقف عليه للتو
        const currentSection = this.sections[this.currentSectionIndex];
        
        // جلب القسم الجديد الذي سننتقل إليه ليصبح هو الظاهر
        const nextSection = this.sections[newIndex];

        /**
         * 3. إزالة حالة النشاط من القسم القديم
         * --------------------------------------------------------------------
         * بإزالة صنف 'scrol-active'، سيبدأ القسم القديم بالاختفاء بناءً 
         * على إعدادات CSS.
         */
        currentSection.classList.remove('scrol-active');
        
        /**
         * 4. تجهيز ترتيب الأقسام للحركة
         * --------------------------------------------------------------------
         * هنا نقرر أي قسم سيتحرك لأعلى وأي قسم سيتحرك لأسفل للحفاظ
         * على المنطق البصري (كأن الصفحات مكدسة فوق بعضها).
         */
        
        if (direction === 'down') {
            
            /**
             * إذا كنا ننزل للأسفل:
             * - القسم القديم يجب أن يُزاح للأعلى ويستقر فوق الشاشة ليفسح المجال.
             * - لذلك نضيف له الصنف 'scrol-up' الذي سيرفعه للأعلى.
             */
            currentSection.classList.add('scrol-up');
            
            /**
             * - القسم القادم يجب أن يأتي من الأسفل ويصعد للمركز.
             * - لذا نتأكد من إزالة أي صنف يجعله في الأعلى (نزيل 'scrol-up' منه).
             */
            nextSection.classList.remove('scrol-up');
            
        } else {
            
            /**
             * إذا كنا نصعد للأعلى:
             * - القسم القديم يجب أن يُزاح للأسفل ويستقر تحت الشاشة.
             * - لذا نتأكد من إزالة الصنف الذي يرفعه للأعلى (نزيل 'scrol-up').
             */
            currentSection.classList.remove('scrol-up');
            
            /**
             * - القسم القادم هو أصلاً موجود بالأعلى (لأننا صعدنا إليه).
             * - يجب أن ينزل لمركز الشاشة، لذا نزيل منه الصنف 'scrol-up'.
             */
            nextSection.classList.remove('scrol-up');
        }

        /**
         * 5. تفعيل القسم الجديد
         * --------------------------------------------------------------------
         * بإضافة الصنف 'scrol-active' للقسم الجديد، سيبدأ في الظهور في مركز الشاشة.
         */
        nextSection.classList.add('scrol-active');
        
        /**
         * 6. تحديث الذاكرة
         * --------------------------------------------------------------------
         * نحدث رقم "القسم الحالي" في ذاكرة المكتبة ليكون هو الرقم الجديد،
         * وبذلك نكون جاهزين للخطوة القادمة في المستقبل.
         */
        this.currentSectionIndex = newIndex;

        /**
         * 7. إعادة فتح القفل بعد انتهاء الحركة
         * --------------------------------------------------------------------
         * نستخدم دالة `setTimeout` وهي عبارة عن مؤقت زمني (مؤجل).
         * نأمرها بأن تنتظر وقتاً يساوي مدة الحركة التي حددها المستخدم 
         * (this.transitionDuration)، وبعد انتهاء هذا الوقت...
         */
        setTimeout(() => {
            
            /**
             * ...تقوم بإعادة فتح القفل بتعيين القيمة `false`.
             * الآن يمكن للمستخدم القيام بتمرير جديد دون مشاكل لأن الحركة انتهت.
             */
            this.isScrolling = false;
            
        }, this.transitionDuration);
    }
}
